# Facit till övning 14.2
# Exempel på en enkel kravspecifikation.

# Program: Filmlista

# Funktionella krav:
# - Programmet ska kunna ta emot filmens namn.
# - Programmet ska kunna ta emot filmens längd i minuter.
# - Programmet ska kunna spara information om en film.
# - Programmet ska kunna visa information om filmen.

# Användarkrav:
# - Användaren ska kunna skriva in filmens namn.
# - Användaren ska kunna skriva in filmens längd.
# - Användaren ska förstå vad som ska matas in.

# Beteende:
# - Programmet ska visa tydliga instruktioner.
# - Programmet ska visa ett felmeddelande om längden inte är ett heltal.
# - Programmet ska inte krascha vid felaktig inmatning.

