# Facit till övning 14.7
# Kort redovisning av programmet

# Vad programmet gör:
# Jag har gjort ett program i Tkinter som hanterar information om en film.
# Användaren skriver in filmens namn och filmens längd i minuter.
# Programmet räknar sedan om tiden till timmar och minuter
# och visar resultatet i fönstret.

# Hur jag planerade arbetet:
# Jag planerade först vad programmet skulle göra.
# Sedan skrev jag vilka krav programmet skulle uppfylla.
# Efter det gjorde jag en enkel design där jag bestämde
# att programmet skulle ha inmatningsfält, knappar
# och en plats där resultatet visas.

# Hur programmet är uppbyggt:
# Programmet är uppbyggt med etiketter, inmatningsfält,
# knappar och en label för resultat.
# Det finns också funktioner i koden.
# En funktion används för att läsa in värden,
# kontrollera att inmatningen är korrekt och visa resultatet.
# En annan funktion används för att rensa fälten.

# Vilka problem jag löste:
# Ett problem jag löste var att användaren kunde skriva
# bokstäver i stället för siffror.
# Det löste jag med try/except.
# Ett annat problem var att användaren kunde lämna filmnamnet tomt.
# Det löste jag genom att kontrollera om textfältet var tomt
# innan programmet fortsatte.

# Hur jag testade programmet:
# Jag testade programmet med flera olika testfall.
# Jag testade med korrekta värden, med tomt namn,
# med bokstäver i längdfältet och med negativa tal.
# På så sätt kunde jag se att programmet fungerade
# och att felmeddelanden visades när det behövdes.

# Vad jag skulle vilja förbättra vidare:
# Om jag skulle förbättra programmet vidare
# skulle jag vilja lägga till stöd för flera filmer.
# Jag skulle också kunna göra gränssnittet snyggare
# och kanske spara filmer i en fil.

# Slutsats:
# Redovisningen visar att jag har planerat, byggt,
# testat och förbättrat mitt program på ett strukturerat sätt.